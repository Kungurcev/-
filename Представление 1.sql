Create view Статистика_Разговоров 
as
SELECT  dbo.Телефоны.Номер_телефона, dbo.Абоненты.ФИО, Количество_Разговоров = COUNT([dbo].[Разговоры].[Номер_разговора])
FROM dbo.Телефоны INNER JOIN dbo.Абоненты ON dbo.Телефоны.Номер_абонента = dbo.Абоненты.Номер_абонента INNER JOIN
                         dbo.Разговоры ON dbo.Телефоны.Код_телефона = dbo.Разговоры.Код_телефона
						